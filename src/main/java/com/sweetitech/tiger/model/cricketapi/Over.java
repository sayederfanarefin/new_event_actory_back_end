package com.sweetitech.tiger.model.cricketapi;

import java.util.List;

public class Over {
	
	int over;
	List<String> balls;
	public int getOver() {
		return over;
	}
	public void setOver(int over) {
		this.over = over;
	}
	public List<String> getBalls() {
		return balls;
	}
	public void setBalls(List<String> balls) {
		this.balls = balls;
	}

}
